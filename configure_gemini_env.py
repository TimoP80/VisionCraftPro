import os
import sys
from pathlib import Path

def configure_gemini_env():
    """Configure Gemini API key in .env file"""
    print("🤖 Configuring Gemini API key for VisionCraft Pro")
    print("=" * 55)
    print()

    # Gemini API key from previous context
