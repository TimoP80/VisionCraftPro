    print(f"🔑 Using Gemini API key: {gemini_key[:20]}...")
    print()

    # Check current directory
    env_file = Path(".env")

    # Read existing .env file if it exists
    existing_env = {}
    if env_file.exists():
        print("📄 Reading existing .env file...")
        try:
            with open(env_file, 'r', encoding='utf-8') as f:
                for line in f:
                    line = line.strip()
                    if line and not line.startswith('#'):
                        if '=' in line:
                            key, value = line.split('=', 1)
                            existing_env[key.strip()] = value.strip()
            print(f"✅ Found {len(existing_env)} existing environment variables")
        except Exception as e:
            print(f"⚠️  Could not read existing .env file: {e}")
    else:
        print("📄 Creating new .env file...")

    print()

    # Update or add Gemini API key
    existing_env["GEMINI_API_KEY"] = gemini_key

    # Write back to .env file
    try:
        with open(env_file, 'w', encoding='utf-8') as f:
            f.write("# VisionCraft Pro Environment Variables\n")
            f.write("# Generated by configure_gemini_env.py\n")
            f.write("\n")
            for key, value in sorted(existing_env.items()):
                f.write(f"{key}={value}\n")

        print("✅ Successfully updated .env file")
        print(f"📍 Location: {env_file.absolute()}")

    except Exception as e:
        print(f"❌ Failed to write .env file: {e}")
        return False

    print()

    # Set environment variable for current session
    os.environ["GEMINI_API_KEY"] = gemini_key
